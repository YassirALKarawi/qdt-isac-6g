"""
Runtime / complexity evaluation (experiment family: runtime).

Measures:
    * per-slot runtime (mean, std, 95% CI)
    * control latency in milliseconds
    * candidate-evaluation count before screening (n_full_candidates)
    * candidate-evaluation count after  screening (q_evals + c_evals)
    * screening overhead in milliseconds
    * a memory-footprint proxy (active RBs, candidate pool size)

Figures generated by the plotting layer:
    * runtime vs candidate-set size
    * runtime vs users and targets
    * search-cost reduction vs shortlist ratio
"""
from __future__ import annotations
from pathlib import Path
from typing import Iterable, List, Optional

import pandas as pd

from config import SimConfig, SWEEP_CONFIGS
from metrics import MetricsCollector
from simulator import run_mc, BL_NAMES

from .common import (apply_profile, ensure_outdir, make_metadata,
                      save_metadata, ExperimentResult)


# Baselines included in the runtime summary table
DEFAULT_BASELINES: List[int] = [0, 1, 2, 3, 4, 5, 6, 7]


DEFAULT_SWEEPS: List[str] = ["candidate_pool", "shortlist_ratio",
                              "user_density", "target_density"]


def run_runtime_experiment(cfg: Optional[SimConfig] = None,
                             baselines: Iterable[int] = DEFAULT_BASELINES,
                             sweeps: Iterable[str] = DEFAULT_SWEEPS,
                             output_dir: Optional[str] = None,
                             verbose: bool = True) -> ExperimentResult:
    cfg = apply_profile(cfg or SimConfig(), "profile_runtime")
    cfg.scenario_name = "runtime"

    outdir = Path(output_dir) if output_dir else ensure_outdir(
        cfg.results_dir, "runtime")

    all_sum, all_sl = [], []
    baselines = list(baselines)

    # Per-baseline runtime table at default configuration
    for bl in baselines:
        c = cfg.clone(baseline_id=bl)
        c.scenario_name = f"runtime_bl{bl}"
        mc = MetricsCollector(steady_state_fraction=c.steady_state_fraction)
        if verbose:
            print(f"\n[runtime] baseline={BL_NAMES.get(bl,bl)}")
        run_mc(c, mc, verbose=verbose, extra={"baseline_id": bl,
                                                 "kind": "per_baseline"})
        sdf = mc.summary_df()
        sldf = mc.slot_df()
        mc.save(str(outdir), prefix=f"bl{bl}_",
                 metadata=make_metadata(c, family="runtime",
                                          scenario=f"runtime_bl{bl}"))
        if not sdf.empty:
            sdf = sdf.copy()
            sdf["kind"] = "per_baseline"
            sdf["baseline_id"] = bl
            all_sum.append(sdf)
        if not sldf.empty:
            sldf = sldf.copy()
            sldf["kind"] = "per_baseline"
            sldf["baseline_id"] = bl
            all_sl.append(sldf)

    # Runtime sweeps (focus on Full Proposed with QA by default)
    for name in sweeps:
        spec = SWEEP_CONFIGS.get(name)
        if spec is None:
            print(f"[runtime] unknown sweep: {name}")
            continue
        param, values = spec["param"], spec["values"]
        for v in values:
            c = cfg.clone(baseline_id=4, **{param: v})
            if c.qa_shortlist_size > c.qa_n_candidates:
                c.qa_shortlist_size = c.qa_n_candidates
            c.scenario_name = f"runtime_{name}_{v}"
            c.sweep_variable = param
            mc = MetricsCollector(steady_state_fraction=c.steady_state_fraction)
            if verbose:
                print(f"\n[runtime] sweep={name} {param}={v}")
            run_mc(c, mc, verbose=verbose,
                    extra={"sweep": name, "sweep_value": v,
                            "baseline_id": 4, "kind": "sweep"})
            sdf = mc.summary_df()
            sldf = mc.slot_df()
            prefix = f"sweep_{name}_{v}_"
            mc.save(str(outdir), prefix=prefix,
                     metadata=make_metadata(c, family="runtime",
                                              scenario=f"runtime_{name}_{v}",
                                              sweep_variable=param))
            if not sdf.empty:
                sdf = sdf.copy()
                sdf["kind"] = "sweep"
                sdf["sweep"] = name
                sdf["sweep_value"] = v
                sdf["baseline_id"] = 4
                all_sum.append(sdf)
            if not sldf.empty:
                sldf = sldf.copy()
                sldf["kind"] = "sweep"
                sldf["sweep"] = name
                sldf["sweep_value"] = v
                sldf["baseline_id"] = 4
                all_sl.append(sldf)

    summary = pd.concat(all_sum, ignore_index=True) if all_sum else pd.DataFrame()
    slots = pd.concat(all_sl, ignore_index=True) if all_sl else pd.DataFrame()
    if not summary.empty:
        summary.to_csv(outdir / "summary.csv", index=False)
    if not slots.empty:
        slots.to_csv(outdir / "slots.csv", index=False)

    md = make_metadata(cfg, family="runtime",
                         scenario="runtime",
                         sweep_variable="runtime_params",
                         extra={"baselines": baselines,
                                 "sweeps": list(sweeps)})
    save_metadata(outdir, md)
    return ExperimentResult(family="runtime", output_dir=outdir,
                              summary=summary, slots=slots, metadata=md)
